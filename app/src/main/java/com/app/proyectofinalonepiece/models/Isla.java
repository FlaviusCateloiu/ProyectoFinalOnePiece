package com.app.proyectofinalonepiece.models;

public class Isla {

    private String id;
    private String nombre;
    private String descripcion;
    private String primeraAparicion;
    private String region;
    private String idPirataDominante;

    public String getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public String getPrimeraAparicion() {
        return primeraAparicion;
    }

    public String getRegion() {
        return region;
    }

    public String getIdPirataDominante() {
        return idPirataDominante;
    }
}
